// create a variable called day , check which day it is using if, else if, and else, print a message for each day ,
//  and print "invalid day" if in the input is not a valid day. //

let day = "Friday";

if (day === "Monday") {
    console.log("Monday - A new week begins. Attend your lectures, organize your notes, and set your academic goals.");
    
} else if (day === "Tuesday") {
    console.log("Tuesday - Stay focused in class, complete your assignments, and keep learning.");

} else if (day === "Wednesday") {
    console.log("Wednesday - It's the middle of the week. Revise what you've learned and prepare for upcoming tests.");

} else if (day === "Thursday") {
    console.log("Thursday - Keep working hard. Participate in class and finish any pending schoolwork.");

} else if (day === "Friday") {
    console.log("Friday - Wrap up your academic tasks, submit assignments, and prepare for the weekend.");

} else if (day === "Saturday") {
    console.log("Saturday - Take some time to rest, study, and work on personal projects or group assignments.");

} else if (day === "Sunday") {
    console.log("Sunday - Relax, revise your notes, and prepare yourself for a productive new week.");

} else {
    console.log("Invalid day.");
}
